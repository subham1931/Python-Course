# \' \' = single quote      
print('I\'m a programmer')
# \" \" = double quote
print("This is a \"string\"")
# \\ \\ = backslash
print("This is a \\backslash")
# \n \n = new line
print("This is a new line \nThis is a new line")
# \t \t = tab
print("This is a tab \tThis is a tab")

# \b \b = backspace
print("This is a backspace \bThis is a backspace")

# print the escape seqence as normal text
print("This is a backspace \\bThis is a backspace")

